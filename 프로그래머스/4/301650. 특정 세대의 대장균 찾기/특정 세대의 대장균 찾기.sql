WITH RECURSIVE r AS (
    -- 1세대
    SELECT ID, PARENT_ID, 1 AS GENERATION
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
    
    UNION ALL
    
    SELECT e.ID, e.PARENT_ID, r.GENERATION + 1 AS GENERATION
    FROM ECOLI_DATA AS e JOIN r ON r.ID = e.PARENT_ID
)

SELECT ID
FROM r
WHERE GENERATION = 3